public class Perimeter {
    public double perimeterG ( double length, double width){
    
        return  (length + width) * 2;
    }
    
    public double areaG(double length1, double width1){
        
        return (length1 * width1);
    }
    public String stringsG(String str1, String str2, String str3){
        
        return str1 + " "+ str2 +" " +str3;
    }
}
   
   
    
    

